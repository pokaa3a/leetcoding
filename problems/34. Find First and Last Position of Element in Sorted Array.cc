/* Solution */
const int MOD = 1000000007;
class Solution {
public:
    vector<int> searchRange(vector<int>& nums, int target) {
        vector<int> output(2, -1);
        if (nums.size() == 0) return output;
        int lo = 0, hi = nums.size() - 1;
        
        while (lo < hi) {
            int mid = lo + (hi - lo) / 2;
            if (nums[mid] < target) {
                lo = mid + 1;
            } else {
                hi = mid;
            }
        }
        if (nums[lo] != target) return output;
        output[0] = lo;
        
        hi = nums.size() - 1;
        while (lo < hi) {
            int mid = lo + (hi - lo + 1) / 2;
            if (nums[mid] <= target) {
                lo = mid;
            } else {
                hi = mid - 1;
            }
        }
        output[1] = lo;
        return output;
    }
};

