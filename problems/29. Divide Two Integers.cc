class Solution {
public:
    int divide(int dividend, int divisor) {
        int sign = (dividend > 0) ^ (divisor > 0) ? -1 : 1;
        long long m = abs(dividend), n = abs(divisor);
        long long ans = 0, tmp = 0;
        
        for (int i = 31; i >= 0; i--) {
            if (tmp + (n << i) <= m) {
                tmp += (n << i);
                ans |= (1ll << i);
            }
        }
        ans *= sign;
        if (ans > INT_MAX) return INT_MAX;
        if (ans < INT_MIN) return INT_MIN;
        return ans;
    }
};

