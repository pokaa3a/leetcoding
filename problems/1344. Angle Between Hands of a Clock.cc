const int MOD = 1000000007;
class Solution {
public:
    double angleClock(int hour, int minutes) {
        double h = double(hour % 12) * 30.0 + double(minutes) * 0.5;
        double m = double(minutes * 6);

        return min(360 - abs(h - m), abs(h - m));
    }
};



