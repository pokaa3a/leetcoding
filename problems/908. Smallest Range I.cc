const int MOD = 1000000007;
class Solution {
public:
    int smallestRangeI(vector<int>& A, int K) {
        int mini = *min_element(A.begin(), A.end());
        int maxi = *max_element(A.begin(), A.end());
        int diff = maxi - mini;
        return max(diff - 2 * K, 0);
    }
};

