void traverse(TreeNode* root) {
	if (!root) return;
	cout << root->val << endl;
	traverse(root->left);
	traverse(root->right);
}

/* Solution */
const int MOD = 1000000007;
class Solution {
public:
    int countOrders(int n) {
        long long nums = 0;
        vector<long long> dp(n + 1, 0);
        dp[1] = 1;

        for (int i = 2; i <= n; ++i) {
        	long long x = 2 * (i - 1);
        	dp[i] = (dp[i - 1] * (x + 2) * (x + 1) / 2) % MOD;
        }
        return (int)dp[n];
    }
};


