class Solution {
public:
    int findLucky(vector<int>& arr) {
        unordered_map<int, int> freq;
        for (int i = 0; i < arr.size(); i++) {
        	freq[arr[i]]++;
        }
		int cand = -1;
        for (int i = 0; i < arr.size(); i++) {
        	if (arr[i] == freq[arr[i]]) {
        		cand = max(cand, arr[i]);
        	}
        }
        return cand;
    }
};



